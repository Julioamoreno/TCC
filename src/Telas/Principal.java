    private void btnExcluirAlbumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirAlbumActionPerformed
        JFrame frame = new JFrame();
        nomeDoAlbum = JOptionPane.showInputDialog(frame, "Digite o nome do album a excluir?","Excluir Album",1);  //Abre uma janela pedindo o nome do album a excluir, e atribui a uma variavel.
           if(nomeDoAlbum!=null) {
                    DeleteCollectionResult excluirPasta = conexao.excluirPasta(nomeDoAlbum);    //Chama a funçao para excluir a pasta, passando o nome da pasta a ser excluida.
                    if (excluirPasta.getStatusCode().equals(200)) { //Codigo 200 é retornado quando a solicitação foi bem sucedida.
                        JOptionPane.showMessageDialog(null, "Pasta "+nomeDoAlbum+" excluída");
                        }
                }
    }//GEN-LAST:event_btnExcluirAlbumActionPerformed

    private void BtnListarFacesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnListarFacesActionPerformed
        try {
            JFrame frame = new JFrame();
            nomeDoAlbum = JOptionPane.showInputDialog(frame, "Digite o nome do album a listar?");
            
            if(nomeDoAlbum.equals("''") || nomeDoAlbum.equals(" ") || nomeDoAlbum.equals("")){ //verifica se o campo foi preenchido.
            JOptionPane.showMessageDialog(null, "Não é possivel encontrar album com nome vazio");
            }
            else{
            JSONArray resposta = conexao.listarFaces(nomeDoAlbum);
            
            Retorno tela = new Retorno(resposta,1); //o primeiro parametro é a resposta retornada e o id que no caso é 1 pois é da função de escrever as faces.
            tela.show();
            }
           
        } catch (JsonProcessingException ex) {
             JOptionPane.showMessageDialog(null, "ERRO : "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JSONException ex) { 
            JOptionPane.showMessageDialog(null, "ERRO : "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnListarFacesActionPerformed

