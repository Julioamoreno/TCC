    private void btnCriarAlbumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarAlbumActionPerformed

            //Cria Album AWS Rekognition
        try {
            JFrame frame = new JFrame();
            nomeDoAlbum = JOptionPane.showInputDialog(frame, "Digite o nome do album ?","Criar Album",1);
           if(nomeDoAlbum!=null){
                      
                CreateCollectionResult criarPasta = conexao.criarPasta(nomeDoAlbum);
                 if (criarPasta.getStatusCode().equals(200)) { //Codigo 200 é retornado quando a solicitação foi bem sucedida.
                    JOptionPane.showMessageDialog(null,"Pasta "+nomeDoAlbum+" criada" );
                }
            }
             

        } catch (Exception ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnCriarAlbumActionPerformed

